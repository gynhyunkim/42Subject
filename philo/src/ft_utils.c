#include "../include/philo.h"

int	ft_atoi(const char *nptr)
{
	int	num;
	int	i;

	i = 0;
	num = 0;
	while (nptr[i] >= '0' && nptr[i] < '9')
	{
		num = num * 10 + (nptr[i] - '0');
		i++;
	}
	return (num);
}
