int		is_printable(char c)
{
	if (c >= 32 && c < 127)
		return (1);
	return (0);
}

int		ft_str_is_printable(char *str)
{
	while (*str != '\0')
	{
		if (!is_printable(*str))
			return (0);
		str++;
	}
	return (1);
}
