int		is_alpha(char c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}

int		ft_str_is_alpha(char *str)
{
	while (*str != '\0')
	{
		if (!is_alpha(*str))
			return (0);
		str++;
	}
	return (1);
}
