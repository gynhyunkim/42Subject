#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*d;

	d = dest;
	while (*src != '\0')
	{
		if (n == 0)
			break ;
		*d = *src;
		d++;
		src++;
		n--;
	}
	while (n != 0)
	{
		*d = '\0';
		d++;
		n--;
	}
	return (dest);
}
