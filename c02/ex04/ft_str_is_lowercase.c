int		is_lowercase(char c)
{
	if (c >= 'a' && c <= 'z')
		return (1);
	return (0);
}

int		ft_str_is_lowercase(char *str)
{
	while (*str != '\0')
	{
		if (!is_lowercase(*str))
			return (0);
		str++;
	}
	return (1);
}
